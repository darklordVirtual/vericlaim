"""Vendor a bundle's code into a project — program with the claim behind you.

The point: the vendored code is BYTE-EXACT the code that produced the
bundle's evidence (hashes come straight from the bundle manifest), so "this
module implements X, proven by CLAIM-Y" is a checkable statement, not a
comment. Three artifacts land in the target:

- ``<dest>/<claim>/...``     the code files, byte-exact, structure preserved
- ``<dest>/<claim>/BINDING.json``   claim id, bundle id, source repo+commit,
                                    and the sha256 of every vendored file
- ``tests/test_bundle_binding_<claim>.py``   a generated test that re-hashes
  the vendored files against BINDING.json — editing the vendored code fails
  the target's own suite, so forking the code is an explicit, visible
  decision (delete the binding test when you consciously fork).

Candidates refuse (unverified assertions do not get to ship code), and a
bundle without code/ refuses rather than vendoring nothing.

    python3 integrations/library/use_code.py --bundle <dir> --target .
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from bundlefmt import load_bundle  # noqa: E402


class UseRefused(ValueError):
    """The bundle's code must not be vendored. Nothing was written."""


_BINDING_TEST = '''\
# SPDX-License-Identifier: Apache-2.0
# GENERATED by vericlaim use_code — do not edit. This test binds the vendored
# code under {dest}/ to bundle {bid12} (claim {cid}): the code you run is
# byte-exact the code that produced the claim's evidence. If you need to
# change the vendored code, you are FORKING it: delete this test and own the
# divergence explicitly.
import hashlib
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
BINDING = ROOT / "{dest}" / "BINDING.json"


def test_vendored_code_matches_bundle_manifest():
    binding = json.loads(BINDING.read_text())
    for rel, expected in sorted(binding["files"].items()):
        p = ROOT / "{dest}" / rel
        assert p.is_file(), f"vendored file missing: {{rel}}"
        actual = hashlib.sha256(p.read_bytes()).hexdigest()
        assert actual == expected, (
            f"{{rel}} was edited after vendoring (hash {{actual[:12]}} != "
            f"bundle {{expected[:12]}}) — fork explicitly by deleting "
            f"tests/{name} or re-vendor the original")
'''


def use_code(bundle_dir: Path, target: Path, *, dest_root: str = "lib") -> dict:
    """Vendor code/ from a verified bundle into *target*. Returns a summary."""
    b = load_bundle(Path(bundle_dir))  # fail-closed verification first
    if b["status"] != "verified":
        raise UseRefused(f"bundle {b['bundle_id'][:12]} is a candidate — an "
                         f"unverified assertion does not get to ship code")
    code_files = {rel: sha for rel, sha in b["manifest"]["files"].items()
                  if rel.startswith("code/")}
    if not code_files:
        raise UseRefused(f"bundle {b['bundle_id'][:12]} has no code/ extract")

    target = Path(target)
    cid = b["claim"]["id"]
    slug = cid.lower().replace("-", "_")
    dest_rel = Path(dest_root) / slug
    dest = target / dest_rel
    if dest.exists():
        raise UseRefused(f"{dest_rel} already exists in the target")

    vendored: dict[str, str] = {}
    for rel, sha in sorted(code_files.items()):
        out_rel = Path(rel).relative_to("code")
        out = dest / out_rel
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_bytes((Path(bundle_dir) / rel).read_bytes())  # byte-exact
        vendored[out_rel.as_posix()] = sha

    prov = b["provenance"]
    binding = {
        "claim_id": cid,
        "bundle_id": b["bundle_id"],
        "statement": b["claim"].get("statement"),
        "evidence_level": b["claim"].get("evidence_level"),
        "caveat": b["claim"].get("caveat"),
        "source_repo": prov.get("source_repo"),
        "source_commit": prov.get("source_commit"),
        "files": vendored,
        "note": "Files are byte-exact from the bundle; the generated binding "
                "test fails if they are edited. Fork explicitly by deleting "
                "the binding test.",
    }
    (dest / "BINDING.json").write_text(
        json.dumps(binding, indent=2, sort_keys=True) + "\n", encoding="utf-8")

    test_name = f"test_bundle_binding_{slug}.py"
    tests_dir = target / "tests"
    tests_dir.mkdir(exist_ok=True)
    (tests_dir / test_name).write_text(_BINDING_TEST.format(
        dest=dest_rel.as_posix(), bid12=b["bundle_id"][:12], cid=cid,
        name=test_name), encoding="utf-8")

    return {"claim_id": cid, "bundle_id": b["bundle_id"],
            "dest": dest_rel.as_posix(), "files": len(vendored),
            "binding_test": f"tests/{test_name}"}


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--bundle", required=True, type=Path)
    ap.add_argument("--target", required=True, type=Path)
    ap.add_argument("--dest", default="lib")
    args = ap.parse_args()
    r = use_code(args.bundle, args.target, dest_root=args.dest)
    print(f"[OK] vendored {r['files']} file(s) from {r['claim_id']} "
          f"(bundle {r['bundle_id'][:12]}) into {r['dest']}/")
    print(f"     binding test: {r['binding_test']} — editing the vendored "
          f"code fails your own suite")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
